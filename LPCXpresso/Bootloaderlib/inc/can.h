#include <stdint.h>

#ifndef CAN_H__
#define CAN_H__

typedef struct {
	uint16_t id;
	uint8_t length;
	uint8_t data[8];
} CanMessage;

typedef enum {
	NO_MESSAGE_RECEIVED = 0,
	MESSAGE_RECEIVED    = 1
} CanReceiveStatus;

void initCan( void );
void deinitCan( void );
CanReceiveStatus canReceive( CanMessage *msg );
void canSend( CanMessage *msg );

#endif
